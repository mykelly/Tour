package com.tour.info;

public class RollListInfo {
	int tourID;
	String tourName;
	String tourFormDate;
	String tourNo;
	String tourDownLoad;

	public int getTourID() {
		return tourID;
	}

	public void setTourID(int tourID) {
		this.tourID = tourID;
	}

	public String getTourName() {
		return tourName;
	}

	public void setTourName(String tourName) {
		this.tourName = tourName;
	}

	public String getTourFormDate() {
		return tourFormDate;
	}

	public void setTourFormDate(String tourFormDate) {
		this.tourFormDate = tourFormDate;
	}

	public String getTourNo() {
		return tourNo;
	}

	public void setTourNo(String tourNo) {
		this.tourNo = tourNo;
	}

	public String getTourDownLoad() {
		return tourDownLoad;
	}

	public void setTourDownLoad(String tourDownLoad) {
		this.tourDownLoad = tourDownLoad;
	}

}
