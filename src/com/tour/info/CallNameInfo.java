package com.tour.info;

public class CallNameInfo {
	String name;
	String seatPostion;
	boolean isStatus;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSeatPostion() {
		return seatPostion;
	}
	public void setSeatPostion(String seatPostion) {
		this.seatPostion = seatPostion;
	}
	public boolean isStatus() {
		return isStatus;
	}
	public void setStatus(boolean isStatus) {
		this.isStatus = isStatus;
	}


}
